from typing import List


class Solution:
    def validMountainArray(self, arr: List[int]) -> bool:
        if len(arr) < 3:
            return False

        i = 1
        while (i < len(arr) and arr[i] > arr[i - 1]):
            i += 1

        if i == 1 or i == len(arr):
            return False

        # At this point, i is at the first "maximum". We now want to test if
        # it's all downhill from there
        # We're hypothetically now at the downhill part:

        while (i < len(arr) and arr[i - 1] > arr[i]):
            i += 1

        # If we haven't reached the end of the array, return false
        if i != len(arr):
            return False

        return True


